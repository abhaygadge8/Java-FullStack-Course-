//Switch case with String value
package practisetest;
class Practise5{
	public static void main(String[] args){
		String choice="b";
		switch(choice){
			case "a":
				System.out.println("Abhay");
				break;
			case "b":
				System.out.println("Abhi");
				break;
			case "c":
				System.out.println("Vicky");
				break;
			default: 
				System.out.println("enter proper choice");
		}
	}
}