//Operators 
let x=10;
let y=20;

console.log("x+y =",x+y);
console.log("x-y =",x-y);
console.log("x*y =",x*y);
console.log("x/y =",x/y);
console.log("x% =",x%y);
console.log("2**8 =",2**8);

//logical Operators
console.log("true && true =",true && true );
console.log("true && false =",true && false);
console.log("false && true =",false && true );
console.log("false && false =",false && false );

//Relational Operator
console.log("x>y =",x>y);
console.log("x<y =",x<y);
console.log("x>=y =",x>=y);
console.log("x<=y =",x<=y);

//shift Operator
console.log("400<<2=",400<<2);
console.log("400>>2=",400>>2);

//== and === program
console.log("5==5 =",5==5);
